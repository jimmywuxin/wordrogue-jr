// 取关卡用的词
export function getWordsForLevel(levelId) {
  const level = LEVELS.find(l => l.id === levelId);
  if (!level) return [];
  return WORDS.filter(w => w.grade === level.grade && w.unit === level.unit);
}

// 选干扰项(同类同 unit,但不是本词)
export function getDistractors(target, count = 3) {
  const sameUnit = WORDS.filter(w => w.unit === target.unit && w.id !== target.id);
  // 随机抽 count 个
  const shuffled = [...sameUnit].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
